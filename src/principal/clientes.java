    private void tbl_datosKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tbl_datosKeyPressed
        int rec = this.tbl_datos.getSelectedRow();
        int code = evt.getKeyCode();
        if (code == KeyEvent.VK_ENTER)
       Principal.txt_id_venta.append(tbl_datos.getValueAt(rec, 0).toString());
       Principal.txt_apellido_venta.append(tbl_datos.getValueAt(rec, 2).toString());
       Principal.txt_nombre_venta.append(tbl_datos.getValueAt(rec, 1).toString());
      Principal.txt_direccion_venta.append(tbl_datos.getValueAt(rec, 3).toString());
      Principal.txt_telefono_venta.append(tbl_datos.getValueAt(rec, 4).toString());
      Principal.txt_referencias_venta.append(tbl_datos.getValueAt(rec, 5).toString());
     setVisible(false);
  //      Principal.txt_referencias_venta.append();
        
    }//GEN-LAST:event_tbl_datosKeyPressed

