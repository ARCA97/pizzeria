    public void DatosTabla(String data){
        if (data == ""){
                Operaciones_cliente oper = new Operaciones_cliente();
                DefaultTableModel model =new DefaultTableModel();
               model = oper.lista();
               tbl_datos.setModel(model);
        }
        else
        {
             Operaciones_cliente oper = new Operaciones_cliente();
                DefaultTableModel model =new DefaultTableModel();
               model = oper.lista_datos(data);
               tbl_datos.setModel(model);
        }
    }

   
    
    
    
    

